package com.thread.waits;
/**
* 获取计算结果并输出
*
* @author leizhimin 2008-9-20 11:15:22
*/
public class ReaderResult extends Thread {
        Calculator c;

        public ReaderResult(Calculator c) {
                this.c = c;
        }

        public void run() {
                synchronized (c) {
                        try {
                                System.out.println(Thread.currentThread() +"_"+ currentThread().getName()+ "等待计算结果。。。");
                                c.wait();
                        } catch (InterruptedException e) {
                                e.printStackTrace();
                        }
                        System.out.println(Thread.currentThread() +"_"+ currentThread().getName()+ "计算结果为：" + c.total);
                }
        }

        public static void main(String[] args) {
                Calculator calculator = new Calculator();

                //启动三个线程，分别获取计算结果
                new ReaderResult(calculator).start();
                new ReaderResult(calculator).start();
                new ReaderResult(calculator).start();
//                //启动计算线程
                calculator.start();
        }
}